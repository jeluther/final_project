// Reset Button
document.querySelector("#reset").addEventListener("click", function() {

    document.querySelector("#fast_five_p").style.display = "none";
    document.querySelector("#risky_business_p").style.display = "none";
    document.querySelector("#scarface_p").style.display = "none";
    document.querySelector("#gone_in_60_seconds_p").style.display = "none";
    document.querySelector("#top_gun_p").style.display = "none";
    document.querySelector("#le_mans_p").style.display = "none";
    document.querySelector("#against_all_odds_p").style.display = "none";
    document.querySelector("#sixteen_candles_p").style.display = "none";
    document.querySelector("#sally_carrera_p").style.display = "none";

});

// The movie Fast Five
document.querySelector("#fast_five").addEventListener("click", function() {

	if (document.querySelector("#fast_five_p").style.display == "none"){

        document.querySelector("#fast_five_p").style.display = "inline";

	}

	else{

        document.querySelector("#fast_five_p").style.display = "none";

	}

});

